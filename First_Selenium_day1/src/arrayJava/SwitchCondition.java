package arrayJava;

public class SwitchCondition {

	public static void main(String[] args) {
		int x = 4;
		switch (x)
		{
		case 1:
			System.out.println("User found --- 1");
			break;
		case 2:
			System.out.println("User found --- 2");
			break;
		case 3:
			System.out.println("User found --- 3");
			break;
		case 4:
			System.out.println("User found --- 4");
			break;
		case 5:
			System.out.println("User found --- 5");
			break;
		default:
			System.out.println("No any number found.....");

		}
	}

}
